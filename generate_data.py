# Фразы, которые мы будем выкидывать чаще (для красивого ТОП-5 в MongoDB)
frequent_phrases = [
    "Ок!",
    "Wow!",
    "Доставка супер ❤️❤️❤️ лучший сервис!",
    "Да",
    "Норм",
    "Фигня полная 💩",
]

data = []
start_date = datetime(2026, 1, 1)

for i in range(100):
    # Диапазон в 5 дней для корректного расчета среднего рейтинга
    random_days = random.randint(0, 4)
    date_str = (start_date + timedelta(days=random_days)).strftime("%Y-%m-%d")

    # 65% шанс взять частую фразу для формирования дубликатов, иначе берем из пула 50 фраз
    if random.random() < 0.65:
        content = random.choice(frequent_phrases)
    else:
        content = None if random.random() < 0.1 else random.choice(phrases)

    user_id = None if random.random() < 0.05 else str(random.randint(10000, 99999))
    age = None if random.random() < 0.1 else str(random.randint(18, 70))
    gender = None if random.random() < 0.1 else random.choice(["M", "F"])
    rating = None if random.random() < 0.15 else f"{random.uniform(1.0, 5.0):.1f}"
    device = random.choice(["iOS", "Android", "Desktop"])
    is_premium = str(random.choice([True, False]))

    data.append([date_str, content, user_id, age, gender, rating, device, is_premium])

columns = [
    "created_date",
    "content",
    "user_id",
    "age",
    "gender",
    "rating",
    "device",
    "is_premium",
]
df = pd.DataFrame(data, columns=columns)
df.to_csv("data.csv", index=False, encoding="utf-8")

